void ECDSA256_Demo(void)
{
	UINT8	  value;
	UINT16  klen, j;  //bytes , not bits
	UINT32  test_bit;

	UINT8 plain[ECDSA_PLAIN_LEN];
	UINT8 pub_key[64];
	UINT8 pri_key[32];
	UINT8 signatureR[32];
	UINT8 signatureS[32];
	
	ECCInit(256);
	test_bit = 256;

	for(j=0; j < ECDSA_PLAIN_LEN; j++)
	{
		plain[j] = ((UINT8)GetRandomWord());//&0x7f;	
	}

	klen = (UINT16)GetRandomWord();

	if((klen > ECDSA_PLAIN_LEN) || (klen == 0))
	{
			klen = ECDSA_PLAIN_LEN;
	}

  SM2_Enable();
	SM2GenerateKeyPair(&stuEccPrams,&stuPrivKey,&stuPubKey);
	
	U32toU8(stuPrivKey.auValue, 8*4, pri_key, 0);
	U32toU8(stuPubKey.stuQx.auValue, 8*4, pub_key, 0);
	U32toU8(stuPubKey.stuQy.auValue, 8*4, pub_key+32, 0);

  printf("PriKey:\r\n");
	for (j = 0; j < 32; j++)
	{
		printf("%02X", pri_key[j]);
	}
	printf("\r\n");
	printf("PubKey:\r\n");
	for (j = 0; j < 64; j++)
	{
		printf("%02X", pub_key[j]);
	}
	printf("\r\n");
	
	//ECDSA
	ECDSA_Sign(plain, klen, HASH_SHA256, &stuPrivKey, &stuSignatureR, &stuSignatureS, &stuEccPrams);
	
	stuSignatureR.uLen = 8;
	stuSignatureS.uLen = 8;

	U32toU8(stuSignatureR.auValue, 8*4, signatureR, 0);
	U32toU8(stuSignatureS.auValue, 8*4, signatureS, 0);
	
	printf("R:\r\n");
	for (j = 0; j < 32; j++)
	{
		printf("%02x", signatureR[j]);
	}
	printf("\r\nS:\r\n");
	for (j = 0; j < 32; j++)
	{
		printf("%02x", signatureS[j]);
	}	
	printf("\r\n");

	stuSignatureR.uLen = 8;
	stuSignatureS.uLen = 8;

	U8toU32(signatureR,32,stuSignatureR.auValue);
	U8toU32(signatureS,32,stuSignatureS.auValue);
	printf("ECDSA %dbits verify(len = %d)...........\r\n", test_bit, klen);
	value = ECDSA_Verify(plain, klen, HASH_SHA256, &stuPubKey, &stuSignatureR, &stuSignatureS, &stuEccPrams);
	if(value == 0)
	{
		printf("ECDSA Random Sign-Verify Fail!\r\n");
		return;
	}
	else
	{
		printf("ECDSA Random Sign-Verify Success!\r\n");
	}
	SM2_Disable();

	return;
}

